package entidad;

public class DashboardSemanaCliente {
	public int lunes, martes, mier, jueve, vierne, sabado, domin;

	public int getLunes() {
		return lunes;
	}

	public void setLunes(int lunes) {
		this.lunes = lunes;
	}

	public int getMartes() {
		return martes;
	}

	public void setMartes(int martes) {
		this.martes = martes;
	}

	public int getMier() {
		return mier;
	}

	public void setMier(int mier) {
		this.mier = mier;
	}

	public int getJueve() {
		return jueve;
	}

	public void setJueve(int jueve) {
		this.jueve = jueve;
	}

	public int getVierne() {
		return vierne;
	}

	public void setVierne(int vierne) {
		this.vierne = vierne;
	}

	public int getSabado() {
		return sabado;
	}

	public void setSabado(int sabado) {
		this.sabado = sabado;
	}

	public int getDomin() {
		return domin;
	}

	public void setDomin(int domin) {
		this.domin = domin;
	}
	
	
}
